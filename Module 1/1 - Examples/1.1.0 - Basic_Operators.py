# Here is the basics to some python

# You can add and multiply pretty easily!
print(5+6)
print(5 * 5)

# Same goes for subtraction and division!
print(6-5)
print(12 / 5)

# You can even do division to get remainders!
print(10 % 3)

# It can even divide to the integer, leaving no remainders
print(10 // 3)

# We can also raise things to a power
print(5 ** 2)
